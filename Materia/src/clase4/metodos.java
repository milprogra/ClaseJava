public class metodos {
    //funcion estatica para resta
    public static int resta(int num1, int num2){
        return num1-num2;
    }
    //funcion estatica para suma
    public static int suma(int num1, int num2){
        return num1+num2;
    }
    //funcion estatica para multiplicacion
    public static int multiplicacion(int num1,int num2){
        return num1*num2;
    }
    //funcion estatica para division
    public static int division(int num1, int num2){
        return num1/num2;
    }
    //funcion para obtener el residual o MOD
    public int residual(int num1, int num2){
        return num1%num2;
    }
    public int residual2(int num1, int num2){
        return num1%num2;
    }
    public int residual3(int num1, int num2){
        return num1%num2;
    }
}
